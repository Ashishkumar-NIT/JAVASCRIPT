// funcitons with arguments and parameters
function addnumber(param1,param2){
    let sum = param1 + param2;
    console.log(`The sum of ${param1} & ${param2} : ${sum}`);
}
addnumber(12,34);
// The sum of 12 & 34 : 46
addnumber(532,3344);
// The sum of 532 & 3344 : 3876
